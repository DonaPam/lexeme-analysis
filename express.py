# Класс для представления лексемы
class Лексема:
    """Структура для хранения информации о лексеме"""

    def __init__(self, значение: str, тип: str, детали: str = ""):
        self.значение = значение
        self.тип = тип
        self.детали = детали if детали else значение

    def __str__(self):
        return f"| {self.значение:<20} | {self.тип:<25} | {self.детали:<20} |"


# Основной класс лексического анализатора
class ЛексическийАнализатор:
    """Класс для лексического анализа входного текста"""

    def __init__(self, текст: str):
        """
        Инициализация анализатора

        Args:
            текст: Входной текст для анализа
        """
        self.текст = текст
        self.позиция = 0
        self.строка = 1
        self.колонка = 1
        self.лексемы: List[Лексема] = []
        self.ошибки: List[str] = []

        # Множества символов для быстрой проверки
        self.цифры = set('0123456789')
        self.операторы = set('+-*/^')
        self.кириллица_буквы = set('абвгдеёжзийклмнопрстуфхцчшщъыьэюяАБВГДЕЁЖЗИЙКЛМНОПРСТУФХЦЧШЩЪЫЬЭЮЯ')

    def текущий_символ(self) -> str:
        """Возвращает текущий символ или пустую строку если конец файла"""
        if self.позиция >= len(self.текст):
            return ''
        return self.текст[self.позиция]

    def следующий_символ(self) -> str:
        """
        Переходит к следующему символу и возвращает его

        Returns:
            Следующий символ или пустую строку если конец файла
        """
        if self.позиция >= len(self.текст):
            return ''

        символ = self.текст[self.позиция]
        self.позиция += 1

        if символ == '\n':
            self.строка += 1
            self.колонка = 1
        else:
            self.колонка += 1

        return символ

    def посмотреть_вперед(self, смещение: int = 1) -> str:
        """
        Смотрит вперед на указанное смещение без изменения позиции

        Args:
            смещение: Количество символов для просмотра вперед

        Returns:
            Символ на указанной позиции или пустую строку
        """
        позиция = self.позиция + смещение - 1
        if позиция >= len(self.текст):
            return ''
        return self.текст[позиция]

    def пропустить_пробелы(self):
        """Пропускает пробельные символы"""
        while self.текущий_символ() and self.текущий_символ().isspace():
            self.следующий_символ()

    def добавить_лексему(self, значение: str, тип: str, детали: str = ""):
        """
        Добавляет лексему в таблицу

        Args:
            значение: Строковое значение лексемы
            тип: Тип лексемы
            детали: Детальная информация (опционально)
        """
        self.лексемы.append(Лексема(значение, тип, детали))

    def добавить_ошибку(self, сообщение: str):
        """
        Добавляет сообщение об ошибке

        Args:
            сообщение: Текст ошибки
        """
        self.ошибки.append(f"Строка {self.строка}, колонка {self.колонка}: {сообщение}")

    def распознать_комментарий(self) -> bool:
        """
        Распознает комментарий в стиле /* ... */

        Returns:
            True если комментарий распознан, иначе False
        """
        if (self.текущий_символ() == '/' and
                self.посмотреть_вперед() == '*'):

            self.следующий_символ()  # пропускаем /
            self.следующий_символ()  # пропускаем *

            while self.текущий_символ():
                if (self.текущий_символ() == '*' and
                        self.посмотреть_вперед() == '/'):
                    self.следующий_символ()  # пропускаем *
                    self.следующий_символ()  # пропускаем /
                    return True
                self.следующий_символ()

            self.добавить_ошибку("Незавершенный комментарий")
            return True

        return False

    def распознать_число(self) -> str:
        """
        Распознает десятичное число (целое или с плавающей точкой)

        Returns:
            Строковое представление числа
        """
        число = ""

        # Читаем целую часть
        while self.текущий_символ() and self.текущий_символ() in self.цифры:
            число += self.следующий_символ()

        # Читаем дробную часть
        if self.текущий_символ() == '.':
            число += self.следующий_символ()

            if not (self.текущий_символ() and self.текущий_символ() in self.цифры):
                self.добавить_ошибка("Некорректное число - ожидаются цифры после точки")

            while self.текущий_символ() and self.текущий_символ() in self.цифры:
                число += self.следующий_символ()

        return число

    def распознать_комплексное(self) -> bool:
        """
        Распознает комплексное число в формате: a+bi, a-bi, bi, -bi

        Returns:
            True если комплексное число распознано, иначе False
        """
        сохран_позиция = self.позиция
        сохран_строка = self.строка
        сохран_колонка = self.колонка

        действит_часть = ""
        мнимая_часть = ""
        знак = '+'

        # Проверяем наличие знака минус в начале
        есть_мин = False
        if self.текущий_символ() == '-':
            есть_мин = True
            действит_часть += self.следующий_символ()

        # Читаем действительную часть (если есть)
        if self.текущий_символ() and self.текущий_символ() in self.цифры:
            действит_часть += self.распознать_число()

        # Проверяем на чисто мнимое число (без действительной части)
        if self.текущий_символ() == 'i':
            # Это чисто мнимое число
            self.следующий_символ()  # пропускаем i

            if действит_часть and действит_часть != '-':
                мнимая_часть = действит_часть
                действит_часть = "0"
            elif есть_мин:
                мнимая_часть = "1"
                действит_часть = "0"
            else:
                мнимая_часть = "1"
                действит_часть = "0"

            # Формируем строку комплексного числа
            if действит_часть == "0" and мнимая_часть:
                комплекс = ("-" if есть_мин else "") + мнимая_часть + "i"
            else:
                комплекс = действит_часть + "+" + мнимая_часть + "i"

            self.добавить_лексему(комплекс, "Комплексное число", комплекс)
            return True

        # Проверяем на комплексное число с действительной и мнимой частями
        if self.текущий_символ() and self.текущий_символ() in '+-':
            сохран_знак_позиция = self.позиция
            сохран_знак_строка = self.строка
            сохран_знак_колонка = self.колонка

            знак_символ = self.текущий_символ()
            self.следующий_символ()  # пропускаем знак

            # Читаем мнимую часть
            мнимая_нач = ""
            есть_мин_мним = False

            if self.текущий_символ() == '-':
                есть_мин_мним = True
                мнимая_нач += self.следующий_символ()

            if self.текущий_символ() and self.текущий_символ() in self.цифры:
                мнимая_часть = self.распознать_число()

                if self.текущий_символ() == 'i':
                    self.следующий_символ()  # пропускаем i

                    # Формируем комплексное число
                    if есть_мин_мним:
                        мнимая_часть = '-' + мнимая_часть

                    комплекс = действит_часть + знак_символ + мнимая_часть + "i"
                    self.добавить_лексему(комплекс, "Комплексное число", комплекс)
                    return True

            # Восстанавливаем позицию если это не комплексное число
            self.позиция = сохран_знак_позиция
            self.строка = сохран_знак_строка
            self.колонка = сохран_знак_колонка

        # Восстанавливаем позицию если это не комплексное число
        self.позиция = сохран_позиция
        self.строка = сохран_строка
        self.колонка = сохран_колонка

        return False

    def распознать_идентификатор(self) -> str:
        """
        Распознает идентификатор (начинается с кириллицы)

        Returns:
            Строковое представление идентификатора
        """
        идентификатор = ""

        while (self.текущий_символ() and
               (self.текущий_символ() in self.кириллица_буквы or
                self.текущий_символ() in self.цифры or
                self.текущий_символ() == '_')):
            идентификатор += self.следующий_символ()

        if len(идентификатор) > 15:
            self.добавить_ошибка(f"Идентификатор слишком длинный (макс. 15): {идентификатор}")

        return идентификатор

    def анализировать(self) -> bool:
        """
        Основной метод анализа

        Returns:
            True если ошибок нет, иначе False
        """
        while self.текущий_символ():
            self.пропустить_пробелы()

            if not self.текущий_символ():
                break

            текущий = self.текущий_символ()

            # Комментарии
            if self.распознать_комментарий():
                continue

            # Разделитель
            if текущий == ';':
                self.добавить_лексему(self.следующий_символ(), "Разделитель", ";")
                continue

            # Скобки
            if текущий == '(':
                self.добавить_лексему(self.следующий_символ(), "Скобка открывающая", "(")
                continue
            if текущий == ')':
                self.добавить_лексему(self.следующий_символ(), "Скобка закрывающая", ")")
                continue

            # Операторы
            if текущий in self.операторы:
                self.добавить_лексему(self.следующий_символ(), "Оператор", текущий)
                continue

            # Оператор присваивания
            if текущий == ':' and self.посмотреть_вперед() == '=':
                self.следующий_символ()  # пропускаем :
                self.следующий_символ()  # пропускаем =
                self.добавить_лексему(":=", "Присваивание", ":=")
                continue

            # Комплексные числа
            if self.распознать_комплексное():
                continue

            # Идентификаторы (кириллица)
            if текущий in self.кириллица_буквы:
                ид = self.распознать_идентификатор()
                if ид:
                    self.добавить_лексему(ид, "Идентификатор", ид)
                continue

            # Неизвестный символ - ошибка
            self.добавить_ошибку(f"Недопустимый символ: '{текущий}'")
            self.следующий_символ()

        return len(self.ошибки) == 0

    def печать_результатов(self):
        """Выводит таблицу лексем и ошибки"""
        print("\n" + "=" * 80)
        print("                     ТАБЛИЦА ЛЕКСЕМ")
        print("=" * 80)
        print(f"| {'Лексема':<20} | {'Тип':<25} | {'Значение':<20} |")
        print("-" * 80)

        for лексема in self.лексемы:
            print(лексема)

        print("-" * 80)

        if self.ошибки:
            print("\n" + "=" * 80)
            print("                     ОБНАРУЖЕНЫ ОШИБКИ")
            print("=" * 80)
            for ошибка in self.ошибки:
                print(f"❌ {ошибка}")
        else:
            print("\n✅ Анализ завершен без ошибок.")

    def печать_статистики(self):
        """Выводит статистику анализа"""
        print("\n" + "=" * 80)
        print("                     СТАТИСТИКА")
        print("=" * 80)
        print(f"Всего лексем: {len(self.лексемы)}")
        print(f"Всего ошибок: {len(self.ошибки)}")

        # Подсчет по типам
        типы = {}
        for лексема in self.лексемы:
            типы[лексема.тип] = типы.get(лексема.тип, 0) + 1

        if типы:
            print("\nРаспределение по типам:")
            for тип, количество in типы.items():
                print(f"  {тип}: {количество}")


